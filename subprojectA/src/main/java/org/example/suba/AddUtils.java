package org.example.suba;

public class AddUtils {
    public static int sum(int x, int y) {
        return x+y;
    }

    public static int diff(int x, int y) {
        return x-y;
    }

    public static boolean isOdd(int x) {
        return x%2 != 0;
    }

    public static boolean isEven(int x) {
        return x%2 == 0;
    }
}
